#!/usr/bin/env pwsh
# Receivers post-provision: rewrite receivers/backend/.env and
# receivers/frontend/.env.production with the values from this azd env.
#
# Mirrors the providers stack pattern: the AKS-side orchestrator deploy
# (build-and-push + kubectl apply) is intentionally not run here; it is
# handled by an explicit follow-up step after azd up succeeds.

param()

$ErrorActionPreference = 'Stop'

Write-Host "Receivers post-provision: rewriting .env files" -ForegroundColor Cyan

$hookDir = Split-Path -Parent $MyInvocation.MyCommand.Path
$receiversDir = Resolve-Path (Join-Path $hookDir '..' '..')
$backendEnvPath = Join-Path $receiversDir 'backend\.env'
$frontendProdEnvPath = Join-Path $receiversDir 'frontend\.env.production'

Write-Host "Loading azd env values..."
$envValues = @{}
$lines = azd env get-values
foreach ($line in $lines) {
  if ($line -match '^\s*([A-Za-z_][A-Za-z0-9_]*)\s*=\s*(.*)$') {
    $key = $matches[1]
    $val = $matches[2].Trim()
    if ($val.StartsWith('"') -and $val.EndsWith('"')) {
      $val = $val.Substring(1, $val.Length - 2)
    }
    $envValues[$key] = $val
  }
}

function Get-Env([string]$name, [string]$default = '') {
  if ($envValues.ContainsKey($name)) { return $envValues[$name] }
  return $default
}

$cosmosEndpoint = Get-Env 'COSMOSDB_ENDPOINT'
$cosmosDb = Get-Env 'COSMOSDB_DATABASE_NAME'
$cosmosAccount = Get-Env 'COSMOSDB_ACCOUNT_NAME'
$cosmosCatalog = Get-Env 'COSMOSDB_CATALOG_COLLECTION' 'catalog'
$cosmosCohorts = Get-Env 'COSMOSDB_COHORTS_COLLECTION' 'cohorts'
$containerRegistry = Get-Env 'CONTAINER_REGISTRY'
$appInsights = Get-Env 'APPLICATIONINSIGHTS_CONNECTION_STRING'
$subscriptionId = Get-Env 'AZURE_SUBSCRIPTION_ID'
$tenantId = Get-Env 'AZURE_TENANT_ID'
$location = Get-Env 'AZURE_LOCATION' 'eastus2'
$rgName = Get-Env 'AZURE_RESOURCE_GROUP_NAME'
$apimGateway = Get-Env 'APIM_GATEWAY_URL'
$sqlServerName = Get-Env 'AZURE_SQL_SERVER_NAME'
$sqlDatabaseName = Get-Env 'AZURE_SQL_DATABASE_NAME'
$sqlServerFqdn = Get-Env 'AZURE_SQL_SERVER_FQDN'
$sqlConnectionString = Get-Env 'AZURE_SQL_CONNECTION_STRING'

$cosmosHost = ''
if ($cosmosEndpoint -match '^https?://([^:/]+)') { $cosmosHost = $matches[1] }

$backendEnvLines = @(
  '# Generated by receivers/_infra/hooks/postprovision.ps1',
  '# Edit fields by hand if you need to override an azd output.',
  "APPINSIGHTS_CONNECTIONSTRING=`"$appInsights`"",
  "AZURE_CONTAINER_REGISTRY_ENDPOINT=`"$containerRegistry`"",
  "AZURE_CONTAINER_REGISTRY_NAME=`"$($containerRegistry -replace '\.azurecr\.io$', '')`"",
  "AZURE_ENV_NAME=`"$(Get-Env 'AZURE_ENV_NAME')`"",
  "AZURE_LOCATION=`"$location`"",
  "AZURE_RESOURCE_GROUP=`"$rgName`"",
  "AZURE_SUBSCRIPTION_ID=`"$subscriptionId`"",
  "AZURE_TENANT_ID=`"$tenantId`"",
  '',
  '# Cosmos DB (receivers stack)',
  "COSMOSDB_DATABASE=`"$cosmosDb`"",
  "COSMOSDB_CATALOG_COLLECTION=`"$cosmosCatalog`"",
  "COSMOSDB_COHORTS_COLLECTION=`"$cosmosCohorts`"",
  "COSMOSDB_HOST=`"$cosmosHost`"",
  "COSMOS_ACCOUNT_NAME=`"$cosmosAccount`"",
  "COSMOS_ENDPOINT=`"$cosmosEndpoint`"",
  '',
  '# Azure SQL reporting database',
  "AZURE_SQL_SERVER_NAME=`"$sqlServerName`"",
  "AZURE_SQL_DATABASE_NAME=`"$sqlDatabaseName`"",
  "AZURE_SQL_SERVER_FQDN=`"$sqlServerFqdn`"",
  "AZURE_SQL_CONNECTION_STRING=`"$sqlConnectionString`"",
  'RECEIVER_REPORTING_SQL_ENABLED=true',
  '',
  '# APIM (kept for parity with providers/submitters)',
  "APIM_GATEWAY_URL=`"$apimGateway`"",
  '',
  '# Local-dev MSAL bypass; never enable in prod',
  'DEVELOPMENT_MODE=true'
)

Write-Host "Writing $backendEnvPath"
$backendEnvLines -join "`n" | Set-Content -Path $backendEnvPath -Encoding utf8 -NoNewline
Add-Content -Path $backendEnvPath -Value "" -NoNewline

if (Test-Path $frontendProdEnvPath) {
  $existing = Get-Content -Path $frontendProdEnvPath
  $kept = $existing | Where-Object { $_ -notmatch '^\s*VITE_APIM_GATEWAY_URL\s*=' }
  $kept += "VITE_APIM_GATEWAY_URL=$apimGateway"
  $kept -join "`n" | Set-Content -Path $frontendProdEnvPath -Encoding utf8 -NoNewline
  Add-Content -Path $frontendProdEnvPath -Value "" -NoNewline
  Write-Host "Updated $frontendProdEnvPath (refreshed VITE_APIM_GATEWAY_URL)"
}
else {
  Write-Host "Skipped frontend .env.production (not present)"
}

Write-Host "Receivers post-provision complete." -ForegroundColor Green
